                <b>What is Javascipt :</b><br>
                avaScript, often abbreviated as JS, is a high-level, dynamic, weakly typed, prototype-based, multi-paradigm, and interpreted programming language. 
                Alongside HTML and CSS, JavaScript is one of the three core technologies of World Wide Web content production.
                It is used to make webpages interactive and provide online programs, including video games. 
                The majority of websites employ it, and all modern web browsers support it without the need for plug-ins by means of a built-in JavaScript engine. 
                Each of the many JavaScript engines represent a different implementation of JavaScript, all based on the ECMAScript specification, with some engines not supporting the spec fully, and with many engines supporting additional features beyond ECMA.
               
